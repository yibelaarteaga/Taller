package puntosiete;

public class person {
	
	private int age; 
	private String name;
	
	public person (int age, String name){
		
		super();
		this.age = age;
		this.name = name;
		}
		
		public String getName() {
			return  name; 
		}
		
		public void setAge(int age) {
			this.age = age;
		}
		
		public int getAge() {
			return age;
		}
	}
 

